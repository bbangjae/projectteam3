package com.spring_boot.projectTeam.model;

public class BookViewVO {
	private String bookId;
	private int bookLike;
	private int bookComment;
	private int bookLook;
	
	public String getBookId() {
		return bookId;
	}
	public void setBookId(String bookId) {
		this.bookId = bookId;
	}
	public int getBookLike() {
		return bookLike;
	}
	public void setBookLike(int bookLike) {
		this.bookLike = bookLike;
	}
	public int getBookComment() {
		return bookComment;
	}
	public void setBookComment(int bookComment) {
		this.bookComment = bookComment;
	}
	public int getBookLook() {
		return bookLook;
	}
	public void setBookLook(int bookLook) {
		this.bookLook = bookLook;
	}
	
	
}

 




